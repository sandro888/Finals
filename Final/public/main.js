function theResponse(response) {
  let jsonObject = JSON.parse(response);
  cityName.innerHTML = jsonObject.name;
  icon.src = "http://openweathermap.org/img/w/" + jsonObject.weather[0].icon + ".png";
  temperature.innerHTML = parseInt(jsonObject.main.temp - 273)  ;
  
  humidity.innerHTML = "Hum." + jsonObject.main.humidity + "%";
  console.log(temperature.innerHTML)
  if(temperature.innerHTML < 20){
    document.body.classList.add('cloudy')
    document.body.classList.remove('sunny')


  }
  else if(temperature.innerHTML >= 20){
    document.body.classList.add('sunny')
    document.body.classList.remove('cloudy')


  }
}

function httpRequestAsync(url, callback)
{
  console.log("hello");
    var httpRequest = new XMLHttpRequest();
    httpRequest.onreadystatechange = () => { 
        if (httpRequest.readyState == 4 && httpRequest.status == 200)
            callback(httpRequest.responseText);
    }
    httpRequest.open("GET", url, true); // true for asynchronous 
    httpRequest.send();
}